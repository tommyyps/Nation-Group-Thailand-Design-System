function stepSortKey(token: string): number {
  const ams = token.match(/u-ams-(\d+)/);
  if (ams) return parseInt(ams[1], 10);
  const tail = token.match(/(\d+)(?!.*\d)/);
  return tail ? parseInt(tail[1], 10) : 0;
}

/** จานสี primitive (ไม่รวม alpha/efc — อยู่ตาราง utility) */
export function buildPrimitivePaletteFamilies(): PaletteFamily[] {
  const byFamily: Record<string, PaletteSwatch[]> = {};

  for (const [token, hex] of Object.entries(FIGMA_PRIMITIVE_HEX)) {
    if (token.includes("/utility/ams/")) {
      if (!byFamily.ams) byFamily.ams = [];
      byFamily.ams.push({
        token,
        hex,
        stepLabel: token.replace("color/component/background/utility/ams/", ""),
      });
      continue;
    }
    const scale = token.match(
      /^color\/(gray-light|gray-dark|bamboo|green|cyan|blue|purple|fuchsia|pink|rose|red|orange|yellow|gold)\/(\d+)$/,
    );
    if (scale) {
      const fam = scale[1];
      if (!byFamily[fam]) byFamily[fam] = [];
      byFamily[fam].push({ token, hex, stepLabel: scale[2] });
      continue;
    }
    if (token.startsWith("color/base/")) {
      if (!byFamily.base) byFamily.base = [];
      byFamily.base.push({
        token,
        hex,
        stepLabel: token.replace("color/base/", ""),
      });
    }
  }

  const baseSwatches: PaletteSwatch[] = [
    { token: "color/base/white", hex: "#ffffff", stepLabel: "white" },
  ];
  if (byFamily.base?.length) {
    for (const s of byFamily.base) baseSwatches.push(s);
  }
  byFamily.base = baseSwatches;

  for (const arr of Object.values(byFamily)) {
    arr.sort((a, b) => stepSortKey(a.token) - stepSortKey(b.token));
  }

  const out: PaletteFamily[] = [];
  for (const id of SCALE_FAMILY_ORDER) {
    const swatches = byFamily[id];
    if (swatches?.length) {
      out.push({ id, label: FAMILY_LABELS[id] ?? id, swatches });
    }
  }
  if (byFamily.ams?.length) {
    out.push({
      id: "ams",
      label: FAMILY_LABELS.ams,
      swatches: byFamily.ams,
    });
  }
  out.push({
    id: "base",
    label: FAMILY_LABELS.base,
    note:
      "ใน Figma อาจใช้ชื่อ primitive/color/base พร้อมสเกลหลายขั้น (เช่น 5–100); ที่นี่แสดง white และ black-100 จาก snapshot",
    swatches: byFamily.base,
  });
  return out;
}

/** Utility = อัลฟา + เอฟเฟกต์ + ชุด AMS */
export function getUtilityTokens(): Record<string, string> {
  const out: Record<string, string> = { ...FIGMA_UTILITY_HEX };
  for (const [k, v] of Object.entries(FIGMA_PRIMITIVE_HEX)) {
    if (k.includes("/utility/ams/")) out[k] = v;
  }
  return out;
}

export type SemanticCategory = "font" | "border" | "foreground" | "background";

export function categorizeSemanticToken(key: string): SemanticCategory {
  if (
    key.startsWith("color/text/") ||
    key.startsWith("color/component/text/")
  ) {
    return "font";
  }
  if (
    key.startsWith("color/border/") ||
    key.startsWith("color/component/border/")
  ) {
    return "border";
  }
  if (
    key.startsWith("color/foreground/") ||
    key.startsWith("color/icon/")
  ) {
    return "foreground";
  }
  if (
    key.startsWith("color/background/") ||
    key.startsWith("color/component/background/")
  ) {
    return "background";
  }
  return "font";
}

const CATEGORY_LABELS: Record<SemanticCategory, string> = {
  font: "Font color",
  border: "Border color",
  foreground: "Foreground color",
  background: "Background color",
};

export type VariableRow = {
  order: number;
  token: string;
  lightHex: string;
  darkHex: string | null;
  detail: string;
};

function tokenDetailTh(token: string): string {
  const tail = token.replace(/^color\/[^/]+\//, "").replace(/\//g, " · ");
  return `ตัวแปรเชิงความหมาย — ${tail}`;
}

export function buildVariableRowsByCategory(): Record<
  SemanticCategory,
  VariableRow[]
> {
  const keys = Object.keys(FIGMA_SEMANTIC_HEX).sort((a, b) =>
    a.localeCompare(b),
  );
  const buckets: Record<SemanticCategory, VariableRow[]> = {
    font: [],
    border: [],
    foreground: [],
    background: [],
  };
  for (const token of keys) {
    const cat = categorizeSemanticToken(token);
    const list = buckets[cat];
    list.push({
      order: list.length + 1,
      token,
      lightHex: FIGMA_SEMANTIC_HEX[token],
      darkHex: FIGMA_DARK_HEX[token] ?? null,
      detail: tokenDetailTh(token),
    });
  }
  return buckets;
}

export function buildUtilityVariableRows(): VariableRow[] {
  const u = getUtilityTokens();
  return Object.keys(u)
    .sort((a, b) => a.localeCompare(b))
    .map((token, i) => ({
      order: i + 1,
      token,
      lightHex: u[token],
      darkHex: FIGMA_DARK_HEX[token] ?? null,
      detail:
        token.includes("/alpha/") || u[token].length > 7
          ? "สีโปร่งแสง (อัลฟา) / ซ้อนบนขาว-ดำในโหมดสว่างและมืด"
          : token.includes("/utility/")
            ? "สียูทิลิตี้สำรองสำหรับคอมโพเนนต์หลายสี เช่น badges"
            : "ค่าสำหรับเอฟเฟกต์ (เช่น เงา)",
    }));
}

